void main() 
{
  //Swapping with Thired Variable:-
  int num1 = 5;
  int num2 = 10;
  //Before Swapping value of num1 and num2:-
  print("The Value of num1 and num2 before Swapping is $num1 and $num2");
  int temp = num1;
  num1 = num2;
  num2 = temp;
  //After Swapping the value of num1 and num2:-
  print("The Value of num1 and num2 After Swapping is $num1 and $num2");
}
